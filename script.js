  function getTranslationValue(language, key) {
    return key.split('.').reduce(function (value, part) {
      return value && value[part] !== undefined ? value[part] : undefined;
    }, translations[language]);
  }

  function translateElements(language, attributeName, targetAttribute) {
    document.querySelectorAll('[' + attributeName + ']').forEach(function (element) {
      var key = element.getAttribute(attributeName);
      var translatedValue = getTranslationValue(language, key);

      if (translatedValue === undefined) {
        return;
      }

      if (targetAttribute) {
        element.setAttribute(targetAttribute, translatedValue);
      } else {
        element.textContent = translatedValue;
      }
    });
  }

  function translatePage(language) {
    translateElements(language, 'data-i18n');
    translateElements(language, 'data-i18n-alt', 'alt');
    translateElements(language, 'data-i18n-placeholder', 'placeholder');
    translateElements(language, 'data-i18n-aria-label', 'aria-label');
  }

  // Reveal on scroll
  var observer;
  if ('IntersectionObserver' in window) {
    observer = new IntersectionObserver(function (entries) {
      entries.forEach(function (entry) {
        if (entry.isIntersecting) {
          entry.target.classList.add('reveal-visible');
          observer.unobserve(entry.target);
        }
      });
    }, { threshold: 0.15 });

    document.querySelectorAll('.reveal').forEach(function (el) {
      if (!el.classList.contains('reveal-visible')) {
        observer.observe(el);
      }
    });
  } else {
    document.querySelectorAll('.reveal').forEach(function (el) {
      el.classList.add('reveal-visible');
    });
  }

